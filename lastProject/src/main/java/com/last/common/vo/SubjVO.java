package com.last.common.vo;

public class SubjVO {
	
	private String subj_code;
	private String subj_nm;
	private int subj_score;
	private String exkind_code;
	
	public String getSubj_code() {
		return subj_code;
	}
	
	public void setSubj_code(String subj_code) {
		this.subj_code = subj_code;
	}
	
	public String getSubj_nm() {
		return subj_nm;
	}
	
	public void setSubj_nm(String subj_nm) {
		this.subj_nm = subj_nm;
	}
	
	public int getSubj_score() {
		return subj_score;
	}
	
	public void setSubj_score(int subj_score) {
		this.subj_score = subj_score;
	}
	
	public String getExkind_code() {
		return exkind_code;
	}
	
	public void setExkind_code(String exkind_code) {
		this.exkind_code = exkind_code;
	}
	
	
}
